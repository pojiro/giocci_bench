defmodule Mix.Tasks.GiocciBench.Visualize.Compare do
  use Mix.Task

  alias GiocciBench.VisualizeCompare

  @shortdoc "Compare multiple benchmark sessions as boxplots"

  @moduledoc """
  Compare multiple benchmark sessions generated by the same benchmark mode
  and create an HTML report with quartile boxplots.

  ## Options

    * `--session-dir` - Session directory to include (repeatable, at least two; wildcard supported)
    * `--out-dir` - Root output directory (default: giocci_bench_output)
    * `--output` - Output HTML path (default: <out_dir>/comparison_<timestamp>/report.html)
    * `--open` - Open generated HTML in default browser
  """

  @impl true
  def run(args) do
    {opts, _rest, _invalid} =
      OptionParser.parse(args,
        switches: [session_dir: :keep, out_dir: :string, output: :string, open: :boolean]
      )

    out_dir = Keyword.get(opts, :out_dir, "giocci_bench_output")
    session_dirs = parse_session_dirs(opts)
    output_path = Keyword.get(opts, :output, VisualizeCompare.default_output_path(out_dir))

    with {:ok, report_path} <- VisualizeCompare.generate_report(session_dirs, output_path) do
      Mix.shell().info("comparison report created: #{report_path}")

      if Keyword.get(opts, :open, false) do
        open_in_browser(report_path)
      end
    else
      {:error, :too_few_sessions} ->
        Mix.raise("at least two --session-dir options are required")

      {:error, {:session_not_found, dir}} ->
        Mix.raise("session directory not found: #{dir}")

      {:error, {:unknown_session_type, dir}} ->
        Mix.raise("cannot detect benchmark type from session: #{dir}")

      {:error, {:no_comparable_csv, dir}} ->
        Mix.raise("no comparable CSV files found in session: #{dir}")

      {:error, {:mixed_session_types, modes}} ->
        mode_names = modes |> Enum.map(&Atom.to_string/1) |> Enum.join(", ")
        Mix.raise("sessions must be generated by the same Mix task, got: #{mode_names}")

      {:error, {:missing_os_info_data, missing}} ->
        details =
          missing
          |> Enum.map(fn {session_name, files} ->
            case files do
              [] -> "#{session_name}: os-info csv not found"
              _ -> "#{session_name}: missing #{Enum.join(files, ", ")}"
            end
          end)
          |> Enum.join("; ")

        Mix.raise("all specified sessions must include --os-info measurement data. #{details}")

      {:error, reason} ->
        Mix.raise("failed to create comparison report: #{inspect(reason)}")
    end
  end

  defp parse_session_dirs(opts) do
    opts
    |> Keyword.get_values(:session_dir)
    |> Enum.map(&String.trim/1)
    |> Enum.reject(&(&1 == ""))
    |> Enum.flat_map(&expand_session_dir/1)
    |> Enum.uniq()
  end

  defp expand_session_dir(path) do
    if String.contains?(path, ["*", "?", "["]) do
      case Path.wildcard(path) do
        [] -> [path]
        matches -> matches
      end
    else
      [path]
    end
  end

  defp open_in_browser(path) do
    command =
      case :os.type() do
        {:unix, :darwin} -> {"open", [path]}
        {:unix, _} -> {"xdg-open", [path]}
        {:win32, _} -> {"cmd", ["/c", "start", "", path]}
      end

    case command do
      {cmd, argv} ->
        case System.cmd(cmd, argv, stderr_to_stdout: true) do
          {_output, 0} ->
            :ok

          {output, _status} ->
            Mix.shell().error("failed to open browser automatically: #{String.trim(output)}")
        end
    end
  rescue
    error ->
      Mix.shell().error("failed to open browser automatically: #{Exception.message(error)}")
  end
end
